 #slicing / selecting sub sequences
# lang = "Python"
# syntax - [start argument : stop argument -1 : step]
print("pratik"[0:5:1])
print("pratik"[0:5:2])
print("pratik"[0::1])
print("pratik"[0::2])
print("pratik"[::2])
print("pratik"[5::-1])
print("pratik"[5::-2])
print("pratik"[-1::-1])  #reverse the string
print("pratik"[::-1]) #  trick 